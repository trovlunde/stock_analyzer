def analyze_test_data(data):
    """
    Analyze test data to get date ranges and counts

    Args:
        data (dict): Dictionary containing test data with 'tests' key
    """
    # Get tests from data dictionary
    tests = data['tests']

    # Convert dates to datetime objects
    recorded_dates = [pd.to_datetime(
        test['recordedDateUtc']) for test in tests]

    # Get date range
    start_date = min(recorded_dates)
    end_date = max(recorded_dates)

    # Count unique profiles
    unique_profiles = set(test['profileId'] for test in tests)

    # Get test counts by type
    test_types = {}
    for test in tests:
        test_type = test['testType']
        test_types[test_type] = test_types.get(test_type, 0) + 1

    print("\nTest Data Analysis")
    print("-" * 50)
    print(
        f"Date Range: {start_date.strftime('%Y-%m-%d %H:%M')} to {end_date.strftime('%Y-%m-%d %H:%M')}")
    print(f"Total Tests: {len(tests)}")
    print(f"Unique Profiles: {len(unique_profiles)}")
    print("\nTests by Type:")
    for test_type, count in test_types.items():
        print(f"  {test_type}: {count}")


